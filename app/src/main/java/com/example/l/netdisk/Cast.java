package com.example.l.netdisk;

public class Cast {
    private String castName;
    private String castPicUrl;
    private String castPicPath;

    public String getCastName() {
        return castName;
    }

    public String getCastPicUrl() {
        return castPicUrl;
    }

    public String getCastPicPath() {
        return castPicPath;
    }

    public void setCastName(String castName) {
        this.castName = castName;
    }

    public void setCastPicUrl(String castPicUrl) {
        this.castPicUrl = castPicUrl;
    }

    public void setCastPicPath(String castPicPath) {
        this.castPicPath = castPicPath;
    }
}
